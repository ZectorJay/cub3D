#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*a;
	size_t	i;
	size_t	size;

	size = ft_strlen(s1);
	if (!(a = malloc((size + 1) * sizeof(char))))
		return (NULL);
	i = 0;
	while (s1[i])
	{
		a[i] = s1[i];
		i++;
	}
	a[i] = '\0';
	return (a);
}
